package mavenp2versionmatch.exception;

public class DBIException extends Exception {

	public DBIException() {
		super();
	}

	public DBIException(String message) {
		super(message);
	}

	public DBIException(Throwable cause) {
		super(cause);
	}

	public DBIException(String message, Throwable cause) {
		super(message, cause);
	}

}
